CREATE TABLE Author(
    
	id INTEGER PRIMARY KEY AUTO_INCREMENT,
    name char(100) NOT NULL
);

CREATE TABLE Genre(
  
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    name char(100) NOT NULL
);

CREATE TABLE Disc(
    
  	id INTEGER PRIMARY KEY AUTO_INCREMENT,
    name char(100) NOT NULL,
    price FLOAT NOT NULL,
    author_id INTEGER NOT NULL,
    genre_id INTEGER NOT NULL,
    FOREIGN KEY(author_id) REFERENCES author(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY(genre_id) REFERENCES genre(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE seller(
  
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    name char(100) NOT NULL
);

CREATE TABLE client(
  
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    name char(100) NOT NULL,
    mail char(50) NOT NULL,
    phone char(15) NOT NULL
);

CREATE TABLE purchase(
  
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    disk_id INTEGER NOT NULL,
    client_id INTEGER NOT NULL,
    seller_id INTEGER NOT NULL,
    FOREIGN KEY(disk_id) REFERENCES disc(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY(client_id) REFERENCES client(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY(seller_id) REFERENCES seller(id) ON UPDATE CASCADE ON DELETE CASCADE
);